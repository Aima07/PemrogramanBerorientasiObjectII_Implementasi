    private void tblDaftarPenjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDaftarPenjualanMouseClicked
        int i = tblDaftarPenjualan.getSelectedRow();
        if(i == -1) {
            return;
        }
        
        String nofaktur = (String)model.getValueAt(i, 0);
        
        if(labelDetail.getText().replaceAll("Detail Penjualan ", "").equals(nofaktur)) {
            labelDetail.setText("Detail Penjualan");
            
            modelDetail.getDataVector().removeAllElements();
            modelDetail.fireTableDataChanged();
        } else {
            labelDetail.setText("Detail Penjualan "+nofaktur);
            
            getDetailData(nofaktur);
        } 
    }//GEN-LAST:event_tblDaftarPenjualanMouseClicked

