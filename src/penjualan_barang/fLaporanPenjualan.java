    public final void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        String awal = formatter.format(Tanggaldari.getModel().getValue());
        String akhir = formatter.format(TanggalSampai.getModel().getValue());

        try {
            Connection c = koneksi.getKoneksi();
            Statement stm = c.createStatement();
            ResultSet sql = stm.executeQuery("SELECT * FROM tbl_penjualan WHERE tanggal BETWEEN '"+awal+"' AND '"+akhir+"' GROUP BY nofaktur");
            
            while(sql.next()) {
                Object[] o = new Object[2];
                o[0] = sql.getString("nofaktur");
                o[1] = sql.getString("tanggal");
                
                model.addRow(o);
            }
            
            sql.close();
            stm.close();
        } catch(SQLException e) {
            System.out.println(e.toString());
        }
    }

    public final void getDetailData(String nofaktur) {
        modelDetail.getDataVector().removeAllElements();
        modelDetail.fireTableDataChanged();

        try {
            Connection c = koneksi.getKoneksi();
            Statement stm = c.createStatement();
            ResultSet sql = stm.executeQuery("SELECT * FROM tbl_penjualan WHERE nofaktur = '" + nofaktur + "'");

            while (sql.next()) {
                Object[] o = new Object[5];
                o[0] = sql.getString("kd_barang");
                o[1] = sql.getString("nama_barang");
                o[2] = sql.getString("hsatuan");
                o[3] = sql.getString("jumlah_jual");
                o[4] = sql.getString("harga");

                modelDetail.addRow(o);
            }

            sql.close();
            stm.close();
        } catch (SQLException e) {
            System.out.println(e.toString());
        }
    }
    

